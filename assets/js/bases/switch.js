const dia = 0;

switch (dia){
    //El case es como si comparara con el === (triple igual)
    case 0:
        console.log('Domingo');
        break;
    case 1:
        console.log('Lunes');
        break;
    case 2:
        console.log('Martes');     
        break;
    default:
        console.log('No es Domingo, Lunes ni Martes');    
}